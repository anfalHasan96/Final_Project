package com.hotel.demo.DepartmentManager;

public class DepartmentManagerInfo {
    String userName;
    String password;
    String name;
    String managed_department;

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getManaged_department() {
        return managed_department;
    }

    public void setManaged_department(String managed_department) {
        this.managed_department = managed_department;
    }
}
